import os
from openai import AsyncOpenAI

# Берем ключ из переменных окружения
client = AsyncOpenAI(
    api_key=os.getenv("OPENROUTER_API_KEY"), 
    base_url="https://openrouter.ai/api/v1",
    default_headers={
        "HTTP-Referer": "https://railway.app", # Для статистики OpenRouter
        "X-Title": "QuizMasterBot"
    }
)

async def generate_quiz_with_ai(user_text):
    """
    Отправляет текст в ИИ и получает структурированный тест.
    """
    system_prompt = (
        "Ты — эксперт по извлечению данных и составитель тестов. "
        "Твоя задача: извлечь вопросы из текста и привести их к СТРОГОМУ формату.\n\n"
        
        "ИНСТРУКЦИИ:\n"
        "1. ИГНОРИРУЙ официальные шапки, ФИО ректоров, названия кафедр и гарантийные письма.\n"
        "2. Если вопросы в тексте идут через запятую или слитно — ОБЯЗАТЕЛЬНО разделяй их на отдельные вопросы.\n"
        "3. Для каждого вопроса выдели 1 ПРАВИЛЬНЫЙ ответ и придумай 2-3 убедительных НЕВЕРНЫХ варианта.\n"
        "4. Если в тексте нет готовых ответов, используй свои знания.\n\n"
        
        "КРИТИЧЕСКИЕ ПРАВИЛА ФОРМАТА:\n"
        "1. ТЕКСТ ВОПРОСА должен быть ЧИСТЫМ. Запрещено ставить символ '#' перед вопросом или внутри него.\n"
        "2. Символ '#' используется ТОЛЬКО ОДИН РАЗ — в начале строки с правильным ответом.\n"
        "3. Не пиши 'Вопрос №', 'Тест:', не используй цифры и точки перед вопросом, если их нет в оригинале.\n\n"
        
        "ОБРАЗЕЦ СТРУКТУРЫ:\n"
        "Текст вопроса без лишних символов\n"
        "# Правильный ответ\n"
        "=====\n"
        "Неверный вариант 1\n"
        "=====\n"
        "Неверный вариант 2\n"
        "+++++\n\n"
        
        "Никаких вступлений или пояснений. Только чистая структура."
    )

    try:
        # 1. ОБРЕЗАЕМ ТЕКСТ. Это критично для избежания ошибки 400!
        # Возьмем первые 10 000 символов - этого хватит на 5-10 страниц.
        safe_text = user_text[:10000] 

        completion = await client.chat.completions.create(
            # ИСПОЛЬЗУЙ ТОЛЬКО ЭТИ ВАРИАНТЫ:
            model="openrouter/auto", # Работает всегда
            # ИЛИ конкретную модель: "google/gemini-flash-1.5"
            
            messages=[
                {"role": "system", "content": system_prompt},
                {"role": "user", "content": f"Сделай тест из этого текста:\n\n{safe_text}"}
            ],
            temperature=0.3 # Стандартное значение
        )
        return completion.choices[0].message.content
    except Exception as e:
        # Если будет ошибка, мы хотя бы увидим её текст в боте
        return f"ERROR_AI: {str(e)}"